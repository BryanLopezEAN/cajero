    private void retiro(int monto){
        if (this.keyUser != 0 && monto <= this.saldo) {
            int retiroSaldo = this.saldo - monto;
            String queryRetiro = "UPDATE transaction SET saldo = " + retiroSaldo + " WHERE id_user = " + this.keyUser;
            try {
                PreparedStatement ps = cn.prepareStatement(queryRetiro);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Se ha descontado de su cuenta la cantidad de " + monto);
                this.saldo = retiroSaldo;
                this.txtSaldoD.setText(String.valueOf(this.saldo));
            } catch (SQLException ex) {
                Logger.getLogger(Cajero.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else {
            JOptionPane.showMessageDialog(null, "Error, verifiquer la información o el monto solicitado excede el saldo actual de su cuenta");
        }
    }

