    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (!this.txtUser.getText().equals("")) {
            String query = "SELECT * FROM user WHERE nombre = '" + this.txtUser.getText() + "';";
            try{    
                Statement st = cn.createStatement();
                ResultSet rs = st.executeQuery(query);
                while(rs.next()){
                    this.keyUser = rs.getInt("id");
                    this.key = rs.getInt("clave");
                }
                
                if (this.keyUser != 0) {
                    JOptionPane.showMessageDialog(null, "Conexión Exitosa");
                    this.txtLogin.setText(this.txtUser.getText());
                    this.txtUser.setText("");
                }else {
                    JOptionPane.showMessageDialog(null, "Usuario no registrado");
                }
            }catch(SQLException ex){
                System.out.println(ex.getMessage());
            }
        }else {
            JOptionPane.showMessageDialog(null, "Ingrese un usuario válido");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnGetSaldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetSaldoActionPerformed
        String querySaldo = "SELECT saldo FROM `transaction` WHERE id_user = " + this.keyUser + ";";
        Statement st;
        try{
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(querySaldo);
            while(rs.next()){
                this.saldo = rs.getInt(1);
                //this.txtSaldoU.setText(String.valueOf(rs.getInt(1)));
            }
            this.txtSaldoU.setText(String.valueOf(this.saldo));
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnGetSaldoActionPerformed

    private void btnUpdateKeyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateKeyActionPerformed
        if (this.keyUser!= 0 && !this.txtKey.getText().equals("") && !this.txtNewKey.getText().equals("") && this.key == Integer.parseInt(this.txtKey.getText())) {
            String queryUpdate = "UPDATE `user` SET `clave` = " + this.txtNewKey.getText() + " WHERE id = " + this.keyUser;
            try {
                PreparedStatement ps = cn.prepareStatement(queryUpdate);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Clave actualizada");
                this.txtKey.setText("");
                this.txtNewKey.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(Cajero.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Error, por favor verifique la información suminstrada");
        }
    }//GEN-LAST:event_btnUpdateKeyActionPerformed

    private void btnDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositarActionPerformed
        if (this.keyUser != 0 && !this.txtDepositar.getText().equals("")) {
            int newSaldo = this.saldo + Integer.parseInt(this.txtDepositar.getText());
            String queryDepo = "UPDATE transaction SET saldo = " + newSaldo + " WHERE id_user = " + this.keyUser;
            try {
                PreparedStatement ps = cn.prepareStatement(queryDepo);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Deposito aceptado");
                this.saldo =  newSaldo;
                this.txtSaldoD.setText(String.valueOf(newSaldo));
                this.txtDepositar.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(Cajero.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else {
            JOptionPane.showMessageDialog(null, "Error, por favor verifique la información suminstrada");
        }
        
    }//GEN-LAST:event_btnDepositarActionPerformed

